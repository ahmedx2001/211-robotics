//                                 User Control Task
//
/////////////////////////////////////////////////////////////////////////////////////////

task usercontrol()
{
	int LeftDrive = 0;
	int RightDrive = 0;
	int BowDrive = 0;
	int IntakeSpeed = 0;
	int IntakeButton = 0;
	int TensionSpeed = 0;
	int TensionTarget = 0;
	int Tension = 0;
	int AutoBow = 0;
	bool JustPressed = false;

	while(1==1)
	{
		Tension = SensorValue(TensionEncoder);
		////// DRIVE STICK //////
		if(abs(vexRT[Ch3]) > 10 ||abs(vexRT[Ch4]) > 10){
			LeftDrive  = (vexRT[Ch3] + vexRT[Ch4]);
			RightDrive   = (vexRT[Ch3] - vexRT[Ch4]);
		}
		else{
			LeftDrive = 0;
			RightDrive = 0;
		}
		BaseSpeed(LeftDrive, RightDrive);

		////// BOW AND TEST //////
		if (abs(vexRT[Ch2]) > 10)
			BowDrive = (vexRT[Ch2]);
		else
			BowDrive = 0;

		BowSpeed(BowDrive+AutoBow);

		/////// INTAKE ////////
		if(vexRT[Btn6U] == 1){
			IntakeSpeed = 127;
		}
		else if(vexRT[Btn6D] == 1){
			if (IntakeButton < 20){
				IntakeSpeed = 0;
			}
			IntakeButton++;
			if (IntakeButton > 20){
				IntakeSpeed   = -127;
			}
		}
		else{
			if(IntakeButton > 10){
				IntakeSpeed = 0;
			}
			IntakeButton = 0;
		}
		motor[Intake] = IntakeSpeed;

		////// DRIVER LOADS ///////
		if (vexRT[Btn5U] == 1){
			if (JustPressed == false){
				JustPressed = true;
				if (AutoBow == 0){
					AutoBow = 85;
				}
				else{
					AutoBow = 0;
				}
			}
		}
		else{
			JustPressed = false;
		}

		//////// TENSION BUTTONS ////////
		if(vexRT[Btn8D] == 1){
			TensionTarget = -1;
			if(vexRT[Btn8L] == 1){
				TensionSpeed = -127;
			}
			else if(vexRT[Btn8R] == 1){
				TensionSpeed = 127;
			}
		}
		else if(vexRT[Btn8L] == 1){
			TensionTarget = 0;
		}
		else if(vexRT[Btn8U] == 1){
			TensionTarget = MidTension;
		}
		else if(vexRT[Btn8R] == 1){
			TensionTarget = CloseTension;
		}

		/////// TENSIONER ///////
		if(TensionTarget >= 0){
			if (Tension < (TensionTarget)){
				TensionSpeed = -127;
			}
			else if (Tension > (TensionTarget+60)){
				TensionSpeed = 127;
			}
			else {
				TensionTarget = -1;
			}
		}
		//Move tensioner and reset speed
		motor[Tensioner] = TensionSpeed;
		TensionSpeed = 0;

		wait1Msec(10);
	}
}
